
public class DivisibleByFive {
    public static void main(String[] args) {
        // Number is divisible by 5 or not
        int num = 15;
        if(num % 5 == 0){
            System.out.println("Number is Divisible by 5");
        } else {
            System.out.println("Number is not Divisible by 5");
        }
    }
}
